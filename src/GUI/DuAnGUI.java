    //Chuyển đổi màu của thẻ đang được click
    private void changeColor() {
        for(int i=0; i<listlbduan.length; i++) {
            if (listtenduan[i].substring(listtenduan[i].length()-1).equals("1")) {
                listlbduan[i].setBackground(Color.green);
            }
            else listlbduan[i].setBackground(Color.gray);
        }
    }
    
    //Event này disable nút xem, sửa, xóa nếu chưa có panel nào được click;
    public void ButtonOn() {
        for(int i=0; i<listlbduan.length; i++)
            if (listtenduan[i].substring(listtenduan[i].length()-1).equals("1")) {
                btnXemDuAn.setEnabled(true);
                btnSuaDuAn.setEnabled(true);
                btnXoaDuAn.setEnabled(true);
                break;
            }
    }
    public void ButtonOff() {
        btnXemDuAn.setEnabled(false);
        btnSuaDuAn.setEnabled(false);
        btnXoaDuAn.setEnabled(false);
    }
    
    //Event này giúp đánh dấu label đang được click bằng cách thêm số 1 ở cuối tên label
    private void addMouseEvent () {
        for(JLabel pn:listlbduan) {
            pn.addMouseListener(new MouseAdapter() {
                public void mouseClicked(MouseEvent e) {
                    String strpn = pn.getText();
                    for (int i=0; i<listtenduan.length; i++) {
                        //kiểm tra tên trên thẻ label được click có trùng khớp với phần tử chứa tên của chính nó không
                        //nếu đúng thì đổi số ở cuối tên phần tử từ 0 thành 1
                        if (strpn.equals(listtenduan[i].substring(0, listtenduan[i].length()-1))) 
                            //nếu đúng đổi status từ 0 thành 1 để biết rằng label này đang được click
                            listtenduan[i] = listtenduan[i].substring(0, listtenduan[i].length()-1) + "1";
                            //nếu sai thì đổi số ở cuối tên phần tử từ 1 thành 0
                        else
                            listtenduan[i] = listtenduan[i].substring(0, listtenduan[i].length()-1) + "0";
                    }
                    changeColor();
                    ButtonOn();
                }
            });
        }
        
        //event này đánh reset trạng thái của tất cả bảng dự án (chưa có bảng nào được click)
        pnDSDuAn.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                for(String strlist:listtenduan) 
                    strlist = strlist.substring(0, strlist.length()-1) + "0";
                for(JLabel pn:listlbduan)
                    pn.setBackground(Color.gray);
                ButtonOff();
            }
        });
    }
