    //Khởi tạo lại các Label dự án dựa trên ArrayList ở DuAnBUS
    public void initDuAn() {
        try {
            if (DuAnBUS.dsda == null || DuAnBUS.dsda.isEmpty()) new DuAnBUS().docDSDA();
            if (ThamgiaDuanBUS.dstongquat == null || ThamgiaDuanBUS.dstongquat.isEmpty()) new ThamgiaDuanBUS().docDSTGDA();
            int num = DuAnBUS.dsda.size();
            listlbduan = new JLabel[num];
            listtenduan = new String[num];
            int i = 0;
            //Chỉnh sửa lại chiều cao của panel tùy theo số lượng dự án
            if (num%2 == 0) pnDSDuAn.setPreferredSize(new Dimension(920, num / 2 * (40 + 220)));
            else if (num%2 != 0) pnDSDuAn.setPreferredSize(new Dimension(920, (num / 2 + 1) * (40 + 220)));
            //Thêm dự án vào panel
            for(DuAnDTO da:DuAnBUS.dsda) {
                JLabel lbduan = new JLabel(da.tenduan);
                lbduan.setPreferredSize(new Dimension(360, 220));
                lbduan.setFont(new Font("Serif", Font.BOLD, 20));
                lbduan.setHorizontalAlignment(JLabel.CENTER);
                lbduan.setVerticalAlignment(JLabel.TOP);
                lbduan.setBackground(Color.gray);
                lbduan.setOpaque(true);
                lbduan.setBorder(BorderFactory.createLineBorder(Color.black, 2));
                listlbduan[i] = lbduan;
                listtenduan[i] = da.tenduan + "0";
                pnDSDuAn.add(listlbduan[i]);    
                i++;
            }
            addMouseEvent();    
        } catch (Exception e) {
            System.out.println(e);
        }  
    }
    
    private int getIDduan() {
        int idduan = -1;
        for(int i=0; i<listtenduan[i].length(); i++) 
            if (listtenduan[i].substring(listtenduan[i].length()-1).equals("1")) {
                idduan = DuAnBUS.dsda.get(i).getIdduan();
                break;
            }
        return idduan;
    }
    
