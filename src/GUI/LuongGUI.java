    private void btnTinhLuongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhLuongActionPerformed
        if (checkField()) {
            double tongluong = 0;
            int lcb = Integer.parseInt(tfLuongCoBan.getText());
            int thuongbc = Integer.parseInt(tfThuongBangCap.getText());
            int thuongda = Integer.parseInt(tfThuongDuan.getText());
            int tienphat = Integer.parseInt(tfKTVP.getText());
            tongluong = (lcb + thuongbc + thuongda - tienphat) * HSL; 
            tfTongLuong.setText(String.valueOf((int) tongluong));
        }
    }//GEN-LAST:event_btnTinhLuongActionPerformed
    
    private boolean checkField() {
        if (tfSoNgayLamViec.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập số ngày làm việc!!", "Lỗi dữ liệu", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!tfSoNgayLamViec.getText().equals("")) try {
            int num = Integer.parseInt(tfSoNgayLamViec.getText().trim());
            //nếu dưới 0 thì cố tình tạo lỗi
            if (num < 0) num = 1000/0;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Dữ liệu không hợp lệ!!", "Lỗi dữ liệu", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
