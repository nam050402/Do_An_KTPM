    private void loadListNV() {
        try {
            if (NhanVienBUS.dsnv == null || NhanVienBUS.dsnv.isEmpty()) new NhanVienBUS().docDSNV();
            if (BangCapBUS.dsbc == null || BangCapBUS.dsbc.isEmpty()) new BangCapBUS().docDSBC();
            if (ThamgiaDuanBUS.dstongquat == null || ThamgiaDuanBUS.dstongquat.isEmpty()) new ThamgiaDuanBUS().docDSTGDA();
            //Khởi tạo table NV
            DefaultTableModel model = (DefaultTableModel) tbNV.getModel();
            for (NhanVienDTO nv:NhanVienBUS.dsnv) 
                if (nv.visible == 1) {
                    Vector row = new Vector();
                    row.add(nv.id); 
                    row.add(nv.name); 
                    if (nv.gioitinh == 1)
                    row.add("Nam"); else row.add("Nữ");
                    row.add(nv.ngaysinh); 
                    row.add(nv.sdt); 
                    row.add(nv.email); 
                    if (nv.tinhtrang == 1)
                    row.add("Còn làm việc"); else row.add("Đã nghỉ làm");
                    model.addRow(row);
                }
            tbNV.setModel(model);
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }
    
    private void addMouseEvent() {
        //thêm event cho frame và table
        pnLuongNV.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                resetForm();
            }
        });
        pnTimKiemLuongNV.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                resetForm();
            }
        });
        tbNV.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                DefaultTableModel model = (DefaultTableModel) tbNV.getModel();
                int rowPos = tbNV.getSelectedRow();
                String idnv = model.getValueAt(rowPos, 0).toString().trim();
                String tennv = model.getValueAt(rowPos, 1).toString().trim();
                ArrayList<ViPham_DTO> listvp = new ViPham_BUS().getAllViPham();
                boolean check = false;
                tfIDNVLuong.setText(idnv);
                tfTenNVLuong.setText(tennv);
                for (NhanVienDTO nv:NhanVienBUS.dsnv)
                    if (nv.id.equals(idnv)) {
                        ImageIcon myimage = new ImageIcon(nv.hinhanh);
                        Image img = myimage.getImage();
                        Image resizeimg = img.getScaledInstance(lbImageLuong.getWidth(), lbImageLuong.getHeight(),Image.SCALE_SMOOTH);
                        lbImageLuong.setIcon(new ImageIcon(resizeimg));
                        tfLuongCoBan.setText(String.valueOf(nv.getLuong() + nv.getPhucap()));
                        for (BangCapDTO bc:BangCapBUS.dsbc)
                            if (nv.id_bangcap == bc.id) {
                                tfThuongBangCap.setText(String.valueOf(bc.thuongbangcap));
                                break;
                            }
                        break;
                    }
                for (ThamgiaDuanDTO tgda:ThamgiaDuanBUS.dstongquat)
                    if (idnv.equals(tgda.idnv)) {
                        int tongtien = 0;
                        tongtien += tgda.thuongduan;
                        tfThuongDuan.setText(String.valueOf(tongtien));
                        check = true;
                    }
                if (check == false) tfThuongDuan.setText("0");
                check = false;
                for (ViPham_DTO vp:listvp)
                    if (idnv.equals(vp.getID_nhanvien()) && vp.getVisible() == 1) {
                        int tongtien = 0;
                        tongtien += vp.getTienphat();
                        tfKTVP.setText(String.valueOf(tongtien));
                        check = true;
                    } 
                if (check == false) tfKTVP.setText("0");
            }
        });
    }
    
